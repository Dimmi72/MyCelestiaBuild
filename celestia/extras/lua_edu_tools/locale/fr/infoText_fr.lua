	["Milky Way"] =
[[La Voie Lactée, notre galaxie :

 La Voie Lactée est un amas gravitationnel d'environ cent
 milliards d'étoiles. Notre Soleil est l'une de ces étoiles et
 se situe environ à une distance de 24 000 années-lumière
 (ou 8000 parsecs) du centre de la Voie Lactée.
 La galaxie possède trois composantes majeures :
  - Un disque fin composé d'étoiles jeunes et intermédiaires.
  Ce disque contient également du gaz et il est très actif
  dans la formation de nouvelles étoiles. Les poussières
  qu'il contient le font apparaître en orange sur l'image COBE.
  La poussière absorbe plus de lumière bleue que de lumière
  rouge et donne ainsi aux étoiles une couleur rougeâtre.
  Notre galaxie possède des bras spiraux situés au niveau
  de son disque. Ces bras spiraux sont des régions très
  actives dans la formation des étoiles.
  - Une barre composée d'étoiles plus âgées (blanches
  sur l'image COBE)
  - Un halo sombre et étendu dont la composition est encore
  inconnue. La matière dans ce halo n'est pas constituée
  d'étoiles lumineuses, c'est pourquoi il n'apparaît pas sur
  l'image COBE. L'existence du halo sombre est déduite
  de son attraction gravitationnelle sur la matière visible.

 (source : www.nasa.gov)]];

