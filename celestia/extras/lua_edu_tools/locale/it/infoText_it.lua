[[La Via Lattea, la nostra galassia :

 La Via Lattea è un ammasso gravitazionale consistente in
 cento miliardi stelle. Il nostro Sole è una queste stelle
 ed è situato ad una distanza di 24 000 anni-luce (8000 parsec)
 dal centro della Via Lattea.
 La galassia possiede tre componenti principali:
 - Un sottile disco composto da stelle giovani ed intermedie
 contenente notevoli quantità di gas; questo disco ed è il più
 attivo nella formazione di nuove stelle. Le polvere appare
 arancione sull'immagine COBE. La polvere assorbe maggiormente 
 la luce blu che la luce rossa donando così alle stelle un colore 
 rossastro. La nostra galassia possiede dei bracci a spirale
 che si dipanano dal disco - queste regioni sono	anch'esse 
 molto attive nella formazione di nuove stelle.
 - Una barra composta di stelle più vecchie (bianca nell'immagine
 COBE).
 - Un alone oscuro ed esteso dalla composizione ancora incerta.
 La materia all'interno di questo alone non è costituita da stelle
 luminose e per questo non appare sull'immagine COBE.
 L'esistenza di questo alone sembra essere dedotta dalla sua
 attrazione gravitazionale sulla materia visibile.
 
