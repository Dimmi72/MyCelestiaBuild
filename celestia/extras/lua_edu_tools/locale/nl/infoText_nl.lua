	["Milky Way"] =
[[De Melkweg, ons eigen Sterrenstelsel:

 De Melkweg is een door de zwaartekracht begrensde
 verzameling van ongeveer 100 miljard sterren.
 Onze Zon is 釪n van die sterren en bevindt zich ongeveer
 24.000 Lichtjaar (8.000 ParSec) verwijderd van het
 midden van de Melkweg.

 Het Sterrenstelsel heeft drie belangrijke componenten:
  - Een dunnen schijf, die uit jonge en daartussen
    liggende oude sterren bestaat - deze schijf bevat
    ook gas waarin zich actief nieuwe sterren vormen.
    Het stof in de schijf zorgt ervoor dat het Sterrenstelsel
    oranje wordt weergegeven. Het stof absorbeert meer blauw
    dan rood licht, waardoor sterren roodachtig lijken te schijnen.
    Ons Sterrenstelsel heeft ook spiraalarmen in de schijf - deze
    spiraalarmen zijn gebieden waarin actieve stervorming plaatsvindt.
  - Een balk met oudere sterren (wit weergegeven in de COBE afbeelding).
  - Een uitgestrekte donkere halo, waarvan de samenstelling
    onbekend is. Daar de materie in een halo niet uit lichtgevende
    sterren bestaat, is deze in de COBE afbeelding ook niet te zien.
    Het bestaan van een donkere halo wordt afgeleid aan de hand van
    de zwaartekrachtsinvloed die het op de zichtbare materie uitoefent.

